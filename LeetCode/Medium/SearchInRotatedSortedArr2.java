  public boolean search(int[] nums, int target) {
    int N = nums.length;
    if (N == 1) {
      if (nums[0] == target)
        return true;
      return false;
    }
    int l = 0;
    int h = N - 1;
    if (nums[l] < nums[h]) {
      // System.out.println("Ascending");
      return binarySearch(l, h, nums, target);
    }

    int centerIndex = getCenterIndex(l, h, N, nums);
    // System.out.println("centerIndex = " + centerIndex + "=========");

    if (centerIndex != 0 && nums[0] <= target && target <= nums[centerIndex - 1]) {
      boolean foundLeft = binarySearch(l, centerIndex - 1, nums, target);
      // System.out.println("lefty = " + foundLeft);
      return foundLeft;
    }

    boolean foundRight = binarySearch(centerIndex, h, nums, target);
    // System.out.println("righty = " + foundRight);
    return foundRight;
  }

  private int getCenterIndex(int l, int h, int N, int[] nums) {
    int center = -1;
    if (nums[0] > nums[1]) {
      center = 1;
    } else if (nums[N - 1] < nums[N - 2]) {
      center = N - 1;
    } else {
      // System.out.println("Fetching center");
      center = findCenterIndex(l, h, nums);
    }
    return center;
  }

